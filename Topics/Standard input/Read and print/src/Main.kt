import java.util.Scanner

fun main() {
    val scanner = Scanner(System.`in`)
    val s1: String = scanner.nextLine()
    val s2: String = scanner.nextLine()
    val num1: Int = scanner.nextInt()
    val num2: Int = scanner.nextInt()

    println(s1)
    println(s2)
    println(num1)
    println(num2)
}